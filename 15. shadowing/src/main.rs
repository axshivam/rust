fn main() {
    let x = 5;
    println!("x == {}", x);
    let x = 'c';
    println!("x == {}", x);
    let x = 67.98;
    println!("x == {}", x);
    let x = "Hello World";
    println!("x == {}", x);
    let x = false;
    println!("x == {}", x);
    let x = [12,3,4,5,6,7];
    println!("x == {:?}", x);
    let x = String::from("New");
    println!("x == {}", x);
    let x = vec![1,2,3];
    println!("x == {:?}", x);

}
